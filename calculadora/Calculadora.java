package calculadora;

public class Calculadora {
	
	public int somar(int x, int y){
		
		int resultado = x + y;
		
		return resultado;
		
	}
	
	public int subtrair(int x, int y){
		
		int resultado = x - y;
		
		return resultado;
		
	}
	
	public double multiplicar(double x, double y){
		
		double outroResultado = x * y;
		
		return outroResultado;
		
	}

}
